<?php

// Определить сложность следующих алгоритмов:

// 1)
$n = 100; 
$array[]= [];

for ($i = 0; $i < $n; $i++) {
    for ($j = 1; $j < $n; $j *= 2) {
        $array[$i][$j]= true;
    } 
}
// Ответ: Цикл в цикле O(n ** 2)


// 2)
$n = 100;
$array[] = [];

for ($i = 0; $i < $n; $i += 2) {
    for ($j = $i; $j < $n; $j++) {
        $array[$i][$j]= true;
    } 
}
// Ответ: Снова цикл в цикле, хоть и с пропусками O(n ** 2)